#1 bir dikdörtgenin kısa ve uzun kenar bilgilerini alıp dikdörtgen çevresini hesaplayan ve geri döndüren fonksiyonu tanımlayınız

def dortgen_cevre_bul(kk:float,uk:float):
    return 2*(kk+uk)
#2 bir dikdörtgenin kısa ve uzun kenar bilgilerini alıp dikdörtgen alanını hesaplayan ve geri döndüren fonksiyonu tanımlayınız
def dortgen_alan_bul(kk:float,uk:float):
    return kk*uk


